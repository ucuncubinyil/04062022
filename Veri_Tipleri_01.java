public class Veri_Tipleri_01 {

    public static void main(String[] args) {

        // Primitif Tipler (İlker Tipler

        //Byte Veri Tipi
        byte myByte = 127; //Maximum değeri
        System.out.println(myByte);

        byte myByte2 =  -128; // Minimum değeri
        System.out.println(myByte2);

        //short Veri Tipi
        short myShort = 32767; //Üst sınırı
        System.out.println(myShort);

        short myShort2 = -32768; //Üst alt sınırı
        System.out.println(myShort2);

        // int (Integer) Veri Tipi
        int myInt = 2147483647; //üst sınırı
        System.out.println(myInt);

        int myInt2 = -2147483648; //alt  sınırı
        System.out.println(myInt2);

        //long Veri Tipi

        long myLong  = 6846846464658555554l;
        System.out.println(myLong);

        //double Veri Tipi
        double myDouble = 23.5;
        System.out.println(myDouble);

        //float Veri Tipi
        float myFloat = 2.2f;

        //Char Veri Tipi
        char karakter = 'A';
        System.out.println(karakter);

        //String Veri Tipi
        String adim = "Mehmet Nuri  ÖZTÜRK";
        System.out.println(adim);
    }

}
