package com.company;

public class WhileDongusu1 {

    /**
     * While Döngüsü
     */

    public static void main(String[] args) {

        /**
         *  while(koşul){
         *      işlemler
         *  }
          */

        int i =  10;

        while (0 < i) {
            System.out.println(i);
            i--;
        }

    }
}
