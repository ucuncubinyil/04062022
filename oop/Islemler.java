package com.mehmetnuri.oop;

public class Islemler {

    int topla(int x, int y) {
        return x + y;
    }

    int cikar(int x, int y) {
        return x - y;
    }

    int carp(int x, int y) {
        return x * y;
    }

    double bol(int x, int y) {
        return (x / y);
    }

}
